public class Product {

    private String productName;
    private String productPrice;
    private String productIntegral;
    private int photoUrl;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductIntegral() {
        return productIntegral;
    }

    public void setProductIntegral(String productIntegral) {
        this.productIntegral = productIntegral;
    }

    public int getPhotoUrl() {
        return photoUrl;
    }

    public void setPhotoUrl(int photoUrl) {
        this.photoUrl = photoUrl;
    }
}
